const Tile = class {
  constructor(hostRef) {
    registerInstance(this, hostRef);
  }
  get hostElement() {
    return getElement(this);
  }
  /**
   * Size of the tile - one of 'small', 'medium' or 'large'
   */
  size = "medium";
  hasHeaderSlot = false;
  hasFooterSlot = false;
  handleHeaderSlotChange() {
    this.hasHeaderSlot = !!this.hostElement.querySelector('[slot="header"]');
  }
  handleFooterSlotChange() {
    this.hasFooterSlot = !!this.hostElement.querySelector('[slot="footer"]');
  }
  render() {
    return h(Host, { key: "c57941571e070a94be7dbbb781950c22a8be3803", class: {
      "tile-small": this.size === "small",
      "tile-medium": this.size === "medium",
      "tile-big": this.size === "big"
    } }, h("div", { key: "3bfa12c4510191a93ae9bdbb811099a4787319ce", class: {
      "tile-header": true,
      "has-content": this.hasHeaderSlot
    } }, h("slot", { key: "d7b682647b2dbbd4a430fa84cf28942497816057", name: "header", onSlotchange: () => this.handleHeaderSlotChange() })), h("div", { key: "1965567486b35ce2ff87e0ffeeaa9b33837f5345", class: "tile-subheader" }, h("slot", { key: "c03ba4fdec33cf0821d1c90a05b344f26a69ff89", name: "subheader" })), h("div", { key: "8e834709f24eba9f1161be7b736f38fabb9b88d9", class: "tile-content" }, h("slot", { key: "69c7deddb575649a4943f3592aceacdf1cd7fcf7" })), h("div", { key: "853de711d9edc48b16290f294d7f88af626ba815", class: {
      "tile-footer": true,
      "has-content": this.hasFooterSlot
    } }, h("slot", { key: "f87ebc8a0de54427400fea4cd37ed2a75eae119f", name: "footer", onSlotchange: () => this.handleFooterSlotChange() })));
  }
};
Tile.style = tileCss();
