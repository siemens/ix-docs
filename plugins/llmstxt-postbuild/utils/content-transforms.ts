export function replaceTypographyTablesWithMarkdown(content: string) {
  const typographyTableRegex =
    /<TypographyTable\b[^>]*typographyName=("([^"]+)"|'([^']+)')[^>]*\/?>(?:<\/TypographyTable>)?/g;

  return content.replace(
    typographyTableRegex,
    (_fullMatch, _attr, doubleQuoted, singleQuoted) => {
      const format = doubleQuoted ?? singleQuoted;
      if (!format) {
        return _fullMatch;
      }

      return buildTypographySnippetBlock(format);
    }
  );
}

function cleanApiValue(value: string) {
  let cleanedValue = value
    .replace(/<ReactMarkdown>/g, '')
    .replace(/<\/ReactMarkdown>/g, '')
    .replace(/\{`([\s\S]*?)`\}/g, '$1')
    .replace(/<DeprecatedTag\s*\/?>/g, 'Deprecated')
    .replace(/<[^>]+>/g, ' ')
    .replace(/\s+/g, ' ');

  cleanedValue = replaceSinceTags(cleanedValue);
  return cleanedValue.trim();
}

function parseApiTableBlock(block: string) {
  const row: Record<string, string> = {};

  const headerMatch = block.match(
    /<ApiTable\.(\w+)Header\b[^>]*name=("([^"]+)"|'([^']+)')[^>]*>([\s\S]*?)<\/ApiTable\.\1Header>/
  );

  if (headerMatch) {
    const headerName = headerMatch[3] ?? headerMatch[4] ?? '';
    const headerInner = cleanApiValue(headerMatch[5] ?? '');
    row.Name = headerName;

    if (headerInner) {
      row.Notes = headerInner;
    }
  }

  const fieldRegex =
    /<ApiTable\.(Text|Code)\b[^>]*name=("([^"]+)"|'([^']+)')[^>]*>([\s\S]*?)<\/ApiTable\.\1>/g;

  for (const fieldMatch of block.matchAll(fieldRegex)) {
    const fieldName = fieldMatch[3] ?? fieldMatch[4] ?? '';
    const fieldValue = cleanApiValue(fieldMatch[5] ?? '');

    if (fieldName && fieldValue) {
      row[fieldName] = fieldValue;
    }
  }

  return Object.keys(row).length > 0 ? row : null;
}

function escapeMarkdownTableValue(value: string) {
  return value.replace(/\|/g, '\\|').replace(/\n/g, '<br/>');
}

function rowsToMarkdownTable(rows: Array<Record<string, string>>) {
  if (rows.length === 0) {
    return '';
  }

  const preferredOrder = [
    'Name',
    'Description',
    'Attribute',
    'Type',
    'Default',
    'Event',
    'Detail',
    'Notes',
  ];

  const existingColumns = new Set<string>();
  for (const row of rows) {
    Object.keys(row).forEach((key) => existingColumns.add(key));
  }

  const orderedColumns = preferredOrder.filter((key) =>
    existingColumns.has(key)
  );
  const remainingColumns = [...existingColumns]
    .filter((key) => !orderedColumns.includes(key))
    .sort();
  const columns = [...orderedColumns, ...remainingColumns];

  if (columns.length === 0) {
    return '';
  }

  const header = `| ${columns.join(' | ')} |`;
  const separator = `| ${columns.map(() => '---').join(' | ')} |`;
  const body = rows.map((row) => {
    const values = columns.map((column) =>
      escapeMarkdownTableValue((row[column] ?? '').trim())
    );
    return `| ${values.join(' | ')} |`;
  });

  return [header, separator, ...body].join('\n');
}

export function replaceApiTablesWithMarkdown(content: string) {
  const apiTableGroupRegex = /(?:<ApiTable\b[\s\S]*?<\/ApiTable>\s*)+/g;

  return content.replace(apiTableGroupRegex, (group) => {
    const blocks = [...group.matchAll(/<ApiTable\b[\s\S]*?<\/ApiTable>/g)].map(
      (match) => match[0]
    );

    const rows = blocks
      .map((block) => parseApiTableBlock(block))
      .filter((row): row is Record<string, string> => !!row);

    const table = rowsToMarkdownTable(rows);
    if (!table) {
      return group;
    }

    return `${table}\n\n`;
  });
}

function extractAttributeValue(block: string, attribute: string) {
  const match = block.match(
    new RegExp(`${attribute}\\s*=\\s*("([^"]*)"|'([^']*)')`)
  );
  return match?.[2] ?? match?.[3] ?? null;
}

function parseApiFunctionParameters(block: string) {
  const paramsBlockMatch = block.match(
    /parameters\s*=\s*\{?\s*\[([\s\S]*?)\]\s*\}?/
  );
  if (!paramsBlockMatch) {
    return '';
  }

  const paramsBlock = paramsBlockMatch[1];
  const params: string[] = [];

  const objectRegex = /\{([\s\S]*?)\}/g;
  for (const objectMatch of paramsBlock.matchAll(objectRegex)) {
    const objectContent = objectMatch[1];
    const paramNameMatch = objectContent.match(/name\s*:\s*['"]([^'"]+)['"]/);
    const paramTypeMatch = objectContent.match(/type\s*:\s*['"]([^'"]+)['"]/);

    if (paramNameMatch && paramTypeMatch) {
      params.push(`${paramNameMatch[1]}: ${paramTypeMatch[1]}`);
    }
  }

  return params.join(', ');
}

function cleanApiFunctionBody(value: string) {
  return value
    .replace(/<[^>]+>/g, ' ')
    .replace(/\s+/g, ' ')
    .trim();
}

export function replaceApiFunctionsWithMarkdown(content: string) {
  const apiFunctionRegex =
    /<ApiFunction\b[\s\S]*?(?:<\/ApiFunction>|(?=\n##\s)|(?=\n###\s)|$)/g;

  return content.replace(apiFunctionRegex, (fullBlock) => {
    const functionName = extractAttributeValue(fullBlock, 'name');
    const returnType = extractAttributeValue(fullBlock, 'returnType') ?? 'void';

    if (!functionName) {
      return fullBlock;
    }

    const parameters = parseApiFunctionParameters(fullBlock);
    const bodyMatch =
      fullBlock.match(/>\s*([\s\S]*?)\s*<\/ApiFunction>/) ??
      fullBlock.match(/>\s*([\s\S]*?)\s*$/);
    const description = cleanApiFunctionBody(bodyMatch?.[1] ?? '');

    const lines = [
      `#### ${functionName}`,
      '',
      '```ts',
      `${functionName}(${parameters}): ${returnType};`,
      '```',
    ];

    if (description) {
      lines.push('', description);
    }

    lines.push('');
    return lines.join('\n');
  });
}
