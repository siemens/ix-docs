export function stripFrontmatter(content: string) {
  return content.replace(/^---\s*\n[\s\S]*?\n---\s*\n?/, '');
}

function escapeRegExp(value: string) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

function extractImportedIdentifiers(importClause: string) {
  const identifiers: string[] = [];
  const clause = importClause.trim();

  const namedImportMatch = clause.match(/\{([^}]+)\}/);
  if (namedImportMatch) {
    const namedImports = namedImportMatch[1]
      .split(',')
      .map((token) => token.trim())
      .filter(Boolean);

    for (const token of namedImports) {
      const aliasMatch = token.match(/^\w+\s+as\s+(\w+)$/);
      if (aliasMatch) {
        identifiers.push(aliasMatch[1]);
      } else {
        identifiers.push(token);
      }
    }
  }

  const defaultImportPart = clause
    .replace(/\{[^}]+\}/, '')
    .replace(',', '')
    .trim();
  if (defaultImportPart) {
    identifiers.push(defaultImportPart);
  }

  return identifiers;
}

export function removeUnusedImports(content: string) {
  const lines = content.split('\n');
  const importLineRegex = /^import\s+(.+?)\s+from\s+['"][^'"]+['"];?\s*$/;
  const nonImportContent = lines
    .filter((line) => !importLineRegex.test(line))
    .join('\n');

  for (let index = 0; index < lines.length; index++) {
    const line = lines[index];
    const importMatch = line.match(importLineRegex);

    if (!importMatch) {
      continue;
    }

    const identifiers = extractImportedIdentifiers(importMatch[1]);
    if (identifiers.length === 0) {
      continue;
    }

    const isUsed = identifiers.some((identifier) => {
      const usageRegex = new RegExp(`\\b${escapeRegExp(identifier)}\\b`, 'm');
      return usageRegex.test(nonImportContent);
    });

    if (!isUsed) {
      lines[index] = '';
    }
  }

  const cleaned = lines
    .join('\n')
    .replace(/\n{3,}/g, '\n\n')
    .trimEnd();

  return `${cleaned}\n`;
}

export function replaceSinceTags(content: string) {
  return content.replace(
    /<SinceTag\s+message=("([^"]+)"|'([^']+)')\s*\/>/g,
    (_fullMatch, _messageAttr, doubleQuotedValue, singleQuotedValue) => {
      const version = doubleQuotedValue ?? singleQuotedValue;
      return `Example is supported since version ${version}`;
    }
  );
}

